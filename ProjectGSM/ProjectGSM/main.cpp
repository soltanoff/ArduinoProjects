/* ========================================================================= */
#include <SoftwareSerial.h>
#include "gsm.h"
/* ========================================================================= */
SoftwareGSM *gsm;
/* ========================================================================= */
void setup()
{
	Serial.begin(115200);  // Скорость порта для связи Arduino с компьютером
	gsm = new SoftwareGSM();
	Serial.println("[GSM] module started.");
}
/* ========================================================================= */
void loop() 
{
	gsm->execute();
}
/* ========================================================================= */