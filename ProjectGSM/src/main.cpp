/* ========================================================================= */
#include "gsm.h"
/* ========================================================================= */
SoftwareGSM *gsm;
/* ========================================================================= */
void setup()
{
	Serial.begin(DEFAULT_SERIAL_PORT);
	gsm = new SoftwareGSM();
	digitalWrite(DEFUALT_POWER_PIN, HIGH);
	Serial.println("[GSM] module started.");
}
/* ========================================================================= */
void loop()
{
	gsm->execute();
}
